# Function to draw the menu
def draw_menu():
    screen.fill(WHITE)
    for index, option in enumerate(menu_options):
        color = BLUE if index == selected_option else BLACK
        text = font.render(option, True, color)
        text_rect = text.get_rect(center=(screen_width // 2, 150 + index * 50))
        screen.blit(text, text_rect)
    pygame.display.flip()

# Function to draw the grid
def draw_grid(grid):
    rows = len(grid)
    cols = len(grid[0])
    cell_width = screen_width // cols
    cell_height = screen_height // rows
    for row in range(rows):
        for col in range(cols):
            color = BLACK if grid[row][col] == 1 else WHITE
            pygame.draw.rect(screen, color, (col * cell_width, row * cell_height, cell_width, cell_height))
            pygame.draw.rect(screen, BLUE, (col * cell_width, row * cell_height, cell_width, cell_height), 1)
    pygame.display.flip()

# Function to draw a single cell state
def draw_state(state, color):
    x, y = state
    cell_width = screen_width // len(grid[0])
    cell_height = screen_height // len(grid)
    pygame.draw.rect(screen, color, (y * cell_width, x * cell_height, cell_width, cell_height))
    pygame.display.flip()

# Function to visualize the algorithm
def visualize_algorithm(algorithm, grid):
    path = algorithm(grid)
    for position in path:
        draw_state(position, GREEN)
        time.sleep(0.1)

# Main loop
running = True
while running:
    for event in pygame.event.get():
        if event.type == pygame.QUIT:
            running = False
        elif event.type == pygame.KEYDOWN:
            if event.key == pygame.K_UP:
                selected_option = (selected_option - 1) % len(menu_options)
            elif event.key == pygame.K_DOWN:
                selected_option = (selected_option + 1) % len(menu_options)
            elif event.key == pygame.K_RETURN:
                if selected_option == len(menu_options) - 1:  # Exit option
                    running = False
                else:
                    screen.fill(WHITE)
                    draw_grid(grid)
                    if selected_option == 0:
                        visualize_algorithm(a_star, grid)
                    elif selected_option == 1:
                        visualize_algorithm(backtracking, grid)
                    elif selected_option == 2:
                        visualize_algorithm(dijkstra, grid)

    draw_menu()

pygame.quit()
sys.exit()
